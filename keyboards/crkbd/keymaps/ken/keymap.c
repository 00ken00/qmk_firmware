// ref: https://okapies.hateblo.jp/entry/2019/02/02/133953
static bool lower_pressed = false;
static bool raise_pressed = false;
static uint16_t key_timer = 0;

bool fire_lang1_lang2(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case LOWER:
      if (record->event.pressed) {
        lower_pressed = true;
        key_timer = timer_read();
        layer_on(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
          
        if (lower_pressed && timer_elapsed(key_timer) < 200) {
          // fire LANG2
          register_code(KC_LANG2);
          unregister_code(KC_LANG2);
        }
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        raise_pressed = true;
        key_timer = timer_read();
        layer_on(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
          
        if (raise_pressed && timer_elapsed(key_timer) < 200) {
          // fire LANG1
          register_code(KC_LANG1);
          unregister_code(KC_LANG1);
        }
      }
      return false;
      break;
    case ADJUST:
      if (record->event.pressed) {
        layer_on(_ADJUST);
      } else {
        layer_off(_ADJUST);
      }
      return false;
      break;
    default:
      if (record->event.pressed) {
        // reset the flag
        lower_pressed = false;
        raise_pressed = false;
      }
      break;
  }
  return true;
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  if (record->event.pressed) {
    set_keylog(keycode, record);
  }
  return fire_lang1_lang2(keycode, record);
}
